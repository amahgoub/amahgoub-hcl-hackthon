package com.ing.hackthon.productGroup;

/** 
 * Data Transfer Object for product groups
 */
public class ProductGroupDTO {

    private int id;

    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
